def bubble_sort(arr):
    n = len(arr)

    for i in range(n):
        # Perulangan untuk membandingkan elemen
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                # Menukar elemen jika urutan salah
                arr[j], arr[j + 1] = arr[j + 1], arr[j]

    return arr

