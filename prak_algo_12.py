def binary_search(arr, target):
    left = 0
    right = len(arr) - 1
    found = False
    index = -1

    while left <= right:
        mid = (left + right) // 2
        if arr[mid] == target:
            found = True
            index = mid
            break
        elif arr[mid] < target:
            left = mid + 1
        else:
            right = mid - 1

    return found, index

